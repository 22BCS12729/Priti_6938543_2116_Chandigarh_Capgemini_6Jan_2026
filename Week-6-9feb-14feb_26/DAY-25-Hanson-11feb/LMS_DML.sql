--q2 query
SELECT DISTINCT M.MEMBER_ID,
                M.MEMBER_NAME
FROM LMS_MEMBERS M
JOIN LMS_BOOK_ISSUE B
    ON M.MEMBER_ID = B.MEMBER_ID
WHERE B.BOOK_ISSUE_STATUS = 'N';


--q3 query
